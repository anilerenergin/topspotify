class CurrentTrack {
  int? timestamp;
  Context? context;
  int? progressMs;
  Item? item;
  String? currentlyPlayingType;
  Actions? actions;
  bool? isPlaying;

  CurrentTrack(
      {this.timestamp,
      this.context,
      this.progressMs,
      this.item,
      this.currentlyPlayingType,
      this.actions,
      this.isPlaying});

  CurrentTrack.fromJson(Map<String, dynamic> json) {
    timestamp = json['timestamp'];
    context =
        json['context'] != null ? new Context.fromJson(json['context']) : null;
    progressMs = json['progress_ms'];
    item = json['item'] != null ? new Item.fromJson(json['item']) : null;
    currentlyPlayingType = json['currently_playing_type'];
    actions =
        json['actions'] != null ? new Actions.fromJson(json['actions']) : null;
    isPlaying = json['is_playing'];
  }

  Map<String, dynamic> toJson() {
    final Map<String, dynamic> data = <String, dynamic>{};
    data['timestamp'] = timestamp;
    if (context != null) {
      data['context'] = context!.toJson();
    }
    data['progress_ms'] = progressMs;
    if (item != null) {
      data['item'] = item!.toJson();
    }
    data['currently_playing_type'] = currentlyPlayingType;
    if (actions != null) {
      data['actions'] = actions!.toJson();
    }
    data['is_playing'] = isPlaying;
    return data;
  }
}

